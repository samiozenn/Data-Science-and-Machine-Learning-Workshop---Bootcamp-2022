# Sanal ortamların listelenmesi:
# conda env list

# Sanal ortamların oluşturulması:
# conda create -n myenv

# Sanal ortamı aktif etme:
# conda activate myenv

# Yüklü paketlerin listelenmesi:
# conda list

# Paket yükleme:
# conda install numpy

# aynı anda birden fazla paket yükleme:
# conda install numpy scipy pandas

# Paket silme:
# conda remove package_name

# Belirli bir versiyona göre paket yükleme:
# conda install numpy=1.20.1


# Paket yükseltme:
# conda upgrade numpy

# Tüm paketleri yükseltme:
# conda upgrade -all


# Bütün kullanılan versiyonları, paketleri, kütüphanleri dışa aktarmak için:
# conda env export > environment.yaml
